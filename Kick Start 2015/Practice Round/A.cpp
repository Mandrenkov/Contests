// Solution
// _____________________________________________________________________________

string read_line() {
    string line;
    getline(cin, line);
    return line;
}

int read_int() {
    return stoi(read_line());
}

void solution() {
    int T = read_int();
    // cin >> T;
    for (int t = 1; t <= T; ++t) {
        int N = read_int();
        // cin >> N;
        vector<string> deck(N);
        for (int i = 0; i < N; ++i) {
            deck[i] = read_line();
            // cin >> deck[i];
        }
        int swaps = 0;
        for (int i = 1; i < N; ++i) {
            if (deck[i - 1] > deck[i]) {
                ++swaps;
                swap(deck[i - 1], deck[i]);
            }
        }
        cout << "Case #" << t << ": " << swaps << '\n';
    }
}
