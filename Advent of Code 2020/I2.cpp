void solution() {
    int N, P;
    cin >> N >> P;
    vector<int> data(N);
    for (int i = 0; i < P + N; ++i) {
        cin >> data[i];
    }
    optional<int> target = nullopt;
    for (int i = P; i < P + N && !target; ++i) {
        bool valid = false;
        for (int j = i - P; j < i && !valid; ++j) {
            for (int k = j + 1; k < i && !valid; ++k) {
                valid = data[j] + data[k] == data[i];
            }
        }
        if (!valid) {
            target = data[i];
        }
    }
    int beg = 0;
    int end = 0;
    int sum = 0;
    while (sum < *target) {
        sum += data[end++];
    }
    while (sum != *target) {
        sum -= data[beg++];
        while (sum < *target) {
            sum += data[end++];
        }
    }
    const auto [min_it, max_it] = minmax_element(data.begin() + beg, data.begin() + end);
    int weakness = *min_it + *max_it;
    cout << weakness << '\n';
}