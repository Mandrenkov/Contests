// Initalize the execution environment and call the solution function.
int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    solution();
}

// -----------------------------------------------------------------------------

void solution() {
    unordered_set<pair<int, int>> flipped;
    string line;
    while (getline(cin, line) && !line.empty()) {
        int x = 0;
        int y = 0;
        for (int i = 0; i < line.size(); ++i) {
            const char c1 = line[i];
            if (c1 == 'e') {
                ++x;
                continue;
            } else if (c1 == 'w') {
                --x;
                continue;
            }
            const char c2 = line[++i];
            if (c1 == 'n') {
                ++y;
            } else if (c1 == 's') {
                --y;
            }
            if (c1 == 's' && c2 == 'w') {
                --x;
            } else if (c1 == 'n' && c2 == 'e') {
                ++x;
            }
        }
        pair<int, int> tile = {x, y};
        if (flipped.find(tile) != flipped.end()) {
            flipped.erase(tile);
        } else {
            flipped.emplace(tile);
        }
    }
    cout << flipped.size() << '\n';
}
